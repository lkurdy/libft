#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		y;
	char	*dest;

	i = 0;
	y = 0;
	while (s[y] != '\0')
		y++;
	dest = malloc(sizeof(char) * y + 1);
	if (!dest)
		return (0);
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
